#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*lst2;

	i = 0;
	lst2 = (t_list *)lst;
	while (lst2)
	{
		i++;
		lst2 = lst2->next;
	}
	return (i);
}
